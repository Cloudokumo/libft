#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*str;
	size_t	i;

	if (!src)
		return (NULL);
	str = (char *)malloc(sizeof(*src) * (ft_strlen(src) + 1));
	if (!str)
		return (NULL);
	i = 0;
	while (src[i])
	{
		str[i] = src[i];
		i++;
	}
	str[i] = 0;
	return (str);
}
